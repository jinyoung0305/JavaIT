package day01;

public class Oper2 {
	public static void main(String[] args) {
		String 글자1 = "Hello";
		String 글자2 = "java";
		String 글자 = 글자1+글자2; //앞에 글자에 뒤에 글자가 붙음
		
		System.out.println(글자);
		
		System.out.println("일\t이"); // 칸 띄우기
		System.out.println("안녕\n하세요"); // 한줄띄우기
		System.out.println("아무개는 말했다 \"ㅎㅇ\""); // " 사용
	}
}
